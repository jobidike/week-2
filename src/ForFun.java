
public class ForFun {

	public static void main(String[] args) {
		
		int counter = 0;                  //while loop examples from YT
		while (counter < 10) {           // 0-9 will print out
			System.out.println(counter);  //this loop will run forever if I don't add counter++
			counter++;                     // ++ adds 1 to the value so when this prints out 
		}
		
		
		
		
		
		do {
			System.out.println(counter);   //do while loop YT
			counter++;
		} while(counter <=10);
		

	}

}
